public class WeightedSample {
	
	double weight;
	boolean e;
	
	public WeightedSample(double weight, boolean e){
		this.weight = weight;
		this.e = e;
	}
	
	public double getWeight(){
		return weight;
	}
	
	public boolean getE(){
		return e;
	}
}
